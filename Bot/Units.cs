        public static uint GetUnit(string name) {
            name = name.ToUpper();
            if (nameToUnit.Count > 0) {
                if (nameToUnit.ContainsKey(name))
                    return nameToUnit[name];
                else
                    throw new System.Exception("Unable to find unit: " + name);
            }
            else {
                foreach (var unitType in Units.All) {
                    if (unitType == SUPPLY_DEPOT_LOWERED) continue;
                    if (unitType == COMMAND_CENTER_FLYING) continue;
                    if (unitType == ORBITAL_COMMAND_FLYING) continue;
                    if (unitType == BARRACKS_FLYING) continue;
                    if (unitType == FACTORY_FLYING) continue;                    
                    if (unitType == STARPORT_FLYING) continue;

                    var unitName = GetName(unitType);
                    if (unitName == "Unit") continue;
                    nameToUnit[unitName.ToUpper()] = unitType;
                }
                return GetUnit(name);
            }
        }

